public class Range{
    public static void main(String[] args) {
        System.out.println("Max value of integer min value = "+Integer.MIN_VALUE);
        System.out.println("Max value of integer max value = "+Integer.MAX_VALUE);
        System.out.println("Max value of float min value = "+Float.MIN_VALUE);
        System.out.println("Max value of float max value = "+Float.MAX_VALUE);
        System.out.println("Max value of sort min value = "+Short.MIN_VALUE);
        System.out.println("Max value of sort max value = "+Short.MAX_VALUE);
        System.out.println("Max value of byte min value = "+Byte.MIN_VALUE);
        System.out.println("Max value of byte max value = "+Byte.MAX_VALUE);
        System.out.println("Max value of long min value = "+Long.MIN_VALUE);
        System.out.println("Max value of long max value = "+Long.MAX_VALUE);
        System.out.println("Max value of double min value = "+Double.MIN_VALUE);
        System.out.println("Max value of double max value = "+Double.MAX_VALUE);
        byte x = -128;
        byte y = (byte)129;
        System.out.println(x);
        System.out.println(y);
    }
}