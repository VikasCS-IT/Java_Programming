import java.util.*;

public class Calculation{
    int sum(int a, int b){
        return a+b;
    }
    int sum(int a, int b, int c){
        return a+b+c;
    }
    void printSum(int a, int b){
        System.out.println(a+b);
    }
    void printSum(String a, String b){
        System.out.println(a+b);
    }
}