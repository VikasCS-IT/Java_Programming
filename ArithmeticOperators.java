public class ArithmeticOperators{
    public static void main(String[] arg){
        int a = 23;
        int b = 45;
        int c = a+b;
        int d = a*b;
        int e = a/b;
        int f = a-b;
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
        System.out.println(c);
    }
}