public class BitwiseOperators{
    public static void main(String[] arf){
        int a = 10;
        int b = 12;
        int c = a & b;
        int d = a | b;
        int e = a >> b;
        int f = a << b;
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
        System.out.println(c);
    }
}